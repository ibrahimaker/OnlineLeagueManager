  static PlayerSkills _generateRandomSkills(Position position) {
    // Pozisyona göre skill değerlerini ayarla
    int baseValue = 60;
    int variation = 20;

    int pace = baseValue +
        (position.isAttacking ? 10 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int shooting = baseValue +
        (position == Position.striker ? 15 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int passing = baseValue +
        (position.isMidfield ? 10 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int dribbling = baseValue +
        (position.isAttacking ? 10 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int defending = baseValue +
        (position.isDefensive ? 15 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int physical = baseValue +
        (position.isDefensive ? 10 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int vision = baseValue +
        (position.isMidfield ? 10 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int finishing = baseValue +
        (position == Position.striker ? 15 : 0) +
        (DateTime.now().millisecondsSinceEpoch % variation);
    int stamina =
        baseValue + (DateTime.now().millisecondsSinceEpoch % variation);
    int leadership =
        baseValue + (DateTime.now().millisecondsSinceEpoch % variation);

    // Değerleri 0-100 arasında sınırla
    return PlayerSkills(
      pace: (pace % 100).clamp(30, 95),
      shooting: (shooting % 100).clamp(25, 95),
      passing: (passing % 100).clamp(30, 95),
      dribbling: (dribbling % 100).clamp(30, 95),
      defending: (defending % 100).clamp(25, 95),
      physical: (physical % 100).clamp(40, 95),
      vision: (vision % 100).clamp(30, 95),
      finishing: (finishing % 100).clamp(25, 95),
      stamina: (stamina % 100).clamp(50, 95),
      leadership: (leadership % 100).clamp(30, 95),
    );
  }
